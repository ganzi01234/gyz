package com.kaixin.android.result;

public class UserInfoResult {
	private int id;
	private String name;
	private String avatar;
	private String gender;
	private String constellation;
	private String signature;
	private String photo_count;
	private String diary_count;
	private String friend_count;
	private String visitor_count;
	private String wallpager;
	private String date;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getConstellation() {
		return constellation;
	}
	public void setConstellation(String constellation) {
		this.constellation = constellation;
	}
	public String getSignature() {
		return signature;
	}
	public void setSignature(String signature) {
		this.signature = signature;
	}
	public String getPhoto_count() {
		return photo_count;
	}
	public void setPhoto_count(String photo_count) {
		this.photo_count = photo_count;
	}
	public String getDiary_count() {
		return diary_count;
	}
	public void setDiary_count(String diary_count) {
		this.diary_count = diary_count;
	}
	public String getFriend_count() {
		return friend_count;
	}
	public void setFriend_count(String friend_count) {
		this.friend_count = friend_count;
	}
	public String getVisitor_count() {
		return visitor_count;
	}
	public void setVisitor_count(String visitor_count) {
		this.visitor_count = visitor_count;
	}
	public String getWallpager() {
		return wallpager;
	}
	public void setWallpager(String wallpager) {
		this.wallpager = wallpager;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
